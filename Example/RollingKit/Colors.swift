import UIKit

extension UIColor {
    
    open class var sky: UIColor {// #00abeb
        get {
            return UIColor(red:0, green: 171/255, blue: 235/255, alpha: 1)
        }
    }
    
    open class var violets: UIColor {// #923982
        get {
            return UIColor(red:146/255, green: 57/255, blue: 130/255, alpha: 1)
        }
    }
    
    open class var grass: UIColor {// #c1d24a
        get {
            return UIColor(red:193/255, green: 210/255, blue: 74/255, alpha: 1)
        }
    }

    open class var rain: UIColor {// #58595b
        get {
            return UIColor(red:88/255, green: 89/255, blue: 91/255, alpha: 1)
        }
    }
}
